// Fit LIVE-record calibration (Platt scaling, sample-size shrunk toward identity)
// from graded predictions in KV, and write src/lib/data/calibration.generated.ts.
//
// Safety by design:
//   • only counts picks logged BEFORE the fight (honest) and actually graded
//   • below MIN_N graded picks → writes identity (a=1,b=0) → model unchanged
//   • shrinks the fit toward identity by w = n/(n+K) so a small record barely
//     moves it; it only becomes meaningful as the public record grows
//   • the transform is monotonic → it never flips which fighter is favored
//
// KV creds: process.env, else .vercel/.env.production.local (CI `vercel pull`),
// else .env.local (local run).
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT = join(ROOT, "src", "lib", "data", "calibration.generated.ts");
const K_PRIOR = 150; // shrinkage strength — full live weight only as n >> 150
const MIN_N = 20;    // below this, don't even attempt a fit

const envFromFile = (p) => {
  try {
    const t = readFileSync(join(ROOT, p), "utf8");
    const o = {};
    for (const m of t.matchAll(/^([A-Z0-9_]+)=(.*)$/gm)) {
      let v = m[2].trim();
      if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
      o[m[1]] = v;
    }
    return o;
  } catch { return {}; }
};
let URL_ = process.env.KV_REST_API_URL || process.env.UPSTASH_REDIS_REST_URL;
let TOK = process.env.KV_REST_API_TOKEN || process.env.UPSTASH_REDIS_REST_TOKEN;
for (const f of [".vercel/.env.production.local", ".env.local"]) {
  if (URL_ && TOK) break;
  const e = envFromFile(f);
  URL_ = URL_ || e.KV_REST_API_URL || e.UPSTASH_REDIS_REST_URL;
  TOK = TOK || e.KV_REST_API_TOKEN || e.UPSTASH_REDIS_REST_TOKEN;
}

const render = (c) =>
  `// AUTO-GENERATED by scripts/gen-calibration.mjs — Platt calibration fit on the
// LIVE graded prediction record, sample-size shrunk toward identity. Identity
// (a=1,b=0) = no effect (small record). Monotonic — never changes the favored side.
/* eslint-disable */
export interface LiveCalibration { a: number; b: number; n: number; updatedAt: string; }
export const LIVE_CALIBRATION: LiveCalibration = ${JSON.stringify(c)};
`;
const writeOut = (c, msg) => { writeFileSync(OUT, render(c)); console.log(msg); };

if (!URL_ || !TOK) {
  writeOut({ a: 1, b: 0, n: 0, updatedAt: new Date().toISOString() }, "calibration: identity (no KV creds)");
  process.exit(0);
}

const redis = async (cmd) => {
  const r = await fetch(URL_, { method: "POST", headers: { Authorization: `Bearer ${TOK}`, "Content-Type": "application/json" }, body: JSON.stringify(cmd) });
  return (await r.json()).result;
};

const ids = (await redis(["SMEMBERS", "pred:index"])) ?? [];
const samples = [];
for (const id of ids) {
  const raw = await redis(["GET", `pred:${id}`]);
  if (!raw) continue;
  let p; try { p = JSON.parse(raw); } catch { continue; }
  if (!p.graded || !p.actualWinnerSide) continue;
  if (new Date(p.loggedAt).getTime() >= new Date(p.date).getTime()) continue; // honest: pre-fight only
  const probA = Number(p.predProbA);
  if (!(probA > 0 && probA < 1)) continue;
  samples.push({ x: Math.log(probA / (1 - probA)), y: p.actualWinnerSide === "A" ? 1 : 0 });
}
const n = samples.length;
if (n < MIN_N) {
  writeOut({ a: 1, b: 0, n, updatedAt: new Date().toISOString() }, `calibration: identity (only ${n} graded picks, need ${MIN_N})`);
  process.exit(0);
}

// Gradient descent: minimize log-loss of sigmoid(a*x + b) vs y.
let a = 1, b = 0; const lr = 0.05;
for (let it = 0; it < 2000; it++) {
  let ga = 0, gb = 0;
  for (const s of samples) { const ph = 1 / (1 + Math.exp(-(a * s.x + b))); const e = ph - s.y; ga += e * s.x; gb += e; }
  a -= lr * ga / n; b -= lr * gb / n;
}
// Shrink toward identity by sample size.
const w = n / (n + K_PRIOR);
let aS = 1 + (a - 1) * w, bS = b * w;
if (!(aS > 0)) { aS = 1; bS = 0; }
writeOut(
  { a: +aS.toFixed(4), b: +bS.toFixed(4), n, updatedAt: new Date().toISOString() },
  `calibration fit: a=${aS.toFixed(4)} b=${bS.toFixed(4)} (raw a=${a.toFixed(3)} b=${b.toFixed(3)}, n=${n}, w=${w.toFixed(3)})`
);
