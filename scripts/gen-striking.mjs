// ============================================================
// FightVex — per-fighter STRIKING PROFILE from real UFCStats fight detail.
// Aggregates each fighter's career significant-strike TARGET (head/body/leg) and
// POSITION (distance/clinch/ground) mix and writes a readable descriptor. This is
// DESCRIPTIVE insight (how a fighter strikes), NOT a prediction input — per the
// validated finding that this detail doesn't improve win prediction.
// Run: node scripts/gen-striking.mjs
// ============================================================
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const { fights } = JSON.parse(readFileSync(join(ROOT, "backtest", "ufcstats-detail.json"), "utf8"));
const norm = (s) => (s || "").toLowerCase().normalize("NFD").replace(/[^a-z]/g, "");

const agg = new Map();
for (const f of fights) for (const s of [f.a, f.b]) {
  const k = norm(s.name); if (!k) continue;
  if (!agg.has(k)) agg.set(k, { name: s.name, fights: 0, head: 0, body: 0, leg: 0, distance: 0, clinch: 0, ground: 0, sig: 0 });
  const a = agg.get(k); a.fights++;
  a.head += s.head || 0; a.body += s.body || 0; a.leg += s.leg || 0;
  a.distance += s.distance || 0; a.clinch += s.clinch || 0; a.ground += s.ground || 0;
  a.sig += (s.head || 0) + (s.body || 0) + (s.leg || 0);
}

function describe(t, p) {
  // t = {head,body,leg} %, p = {distance,clinch,ground} % — pick the standout trait.
  if (p.ground >= 28) return { label: "Ground striker", blurb: `Grappler's striking — ${p.ground}% of significant strikes land on the mat.` };
  if (t.leg >= 27) return { label: "Leg-kick specialist", blurb: `Chops the legs — ${t.leg}% of strikes target the legs, mostly at range.` };
  if (p.clinch >= 20) return { label: "Clinch fighter", blurb: `Thrives in tight — ${p.clinch}% of strikes land in the clinch.` };
  if (t.head >= 78 && p.distance >= 88) return { label: "Range head-hunter", blurb: `Pure distance boxing — ${t.head}% to the head, ${p.distance}% at range.` };
  if (t.body >= 27) return { label: "Body puncher", blurb: `Invests downstairs — ${t.body}% of strikes target the body.` };
  if (t.leg >= 20 && p.distance >= 80) return { label: "Range striker", blurb: `Long-range attack — ${p.distance}% at distance, mixing levels.` };
  return { label: "Well-rounded striker", blurb: `Balanced attack — ${t.head}% head / ${t.body}% body / ${t.leg}% leg, mostly at range.` };
}

const out = {};
let kept = 0;
for (const [k, a] of agg) {
  if (a.fights < 3 || a.sig < 30) continue; // need a stable sample
  const tt = a.head + a.body + a.leg, pp = a.distance + a.clinch + a.ground;
  if (!tt || !pp) continue;
  const t = { head: Math.round(a.head / tt * 100), body: Math.round(a.body / tt * 100), leg: Math.round(a.leg / tt * 100) };
  const p = { distance: Math.round(a.distance / pp * 100), clinch: Math.round(a.clinch / pp * 100), ground: Math.round(a.ground / pp * 100) };
  const { label, blurb } = describe(t, p);
  out[k] = { fights: a.fights, sigPerFight: +(a.sig / a.fights).toFixed(1), ...t, ...{ distance: p.distance, clinch: p.clinch, ground: p.ground }, label, blurb };
  kept++;
}

writeFileSync(join(ROOT, "src", "lib", "data", "striking.generated.ts"),
`// AUTO-GENERATED by scripts/gen-striking.mjs from ${fights.length} real UFCStats
// fights. DESCRIPTIVE striking insight (target/position mix), NOT a prediction
// input. Keyed by normalized fighter name. DO NOT EDIT BY HAND.
/* eslint-disable */
export interface StrikingProfile { fights: number; sigPerFight: number; head: number; body: number; leg: number; distance: number; clinch: number; ground: number; label: string; blurb: string; }
export const STRIKING: Record<string, StrikingProfile> = ${JSON.stringify(out)};
const norm = (s: string) => (s || "").toLowerCase().normalize("NFD").replace(/[^a-z]/g, "");
export function strikingFor(name: string): StrikingProfile | null { return STRIKING[norm(name)] ?? null; }
`);
console.log(`Wrote striking.generated.ts: ${kept} fighter profiles from ${fights.length} fights`);
